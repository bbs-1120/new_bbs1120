/**
 * 広告アカウント名から広告主IDを取得
 * 完全一致がなければ部分一致で検索
 */
export function getAdvertiserIdByAccountName(accountName: string): string | null {
  // 完全一致
  if (TIKTOK_ADVERTISER_MAP[accountName]) {
    return TIKTOK_ADVERTISER_MAP[accountName];
  }
  
  // 部分一致（前方一致優先）
  const normalizedName = accountName.trim();
  for (const [key, value] of Object.entries(TIKTOK_ADVERTISER_MAP)) {
    if (key.startsWith(normalizedName) || normalizedName.startsWith(key)) {
      return value;
    }
  }
  
  // 部分一致（キーワード含む）
  for (const [key, value] of Object.entries(TIKTOK_ADVERTISER_MAP)) {
    if (key.includes(normalizedName) || normalizedName.includes(key)) {
      return value;
    }
  }
  
  return null;
}

